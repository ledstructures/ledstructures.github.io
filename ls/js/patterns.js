function getRandomInt(max) {
    return Math.floor(Math.random() * max);
}

function randomNoDoubles(len) {
    let arr =[];
    for (let i = 0; i < len; i++) {
        let nw = getRandomInt(len);
        while (arr.includes(nw)) {
            nw = getRandomInt(len);
        }
        arr.push(nw);
    }
    for (let i = 0; i < len; i++) {
        arr[i] = arr[i] * 4;
    }
    return arr;
}

function randomDoubles(len) {
    let arr =[];
    for (let i = 0; i < len; i++) {
        let nw = getRandomInt(len);
        arr.push(nw);
    }
    for (let i = 0; i < len; i++) {
        arr[i] = arr[i] * 4;
    }
    return arr;
}